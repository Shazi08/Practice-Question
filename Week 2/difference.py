# Explain the difference between dynamic typing in Python and static typing in C/Java.
#Dynamic Typing in Python:
#1.Type of a variable is declared at run time.
#2.No need to Declare Type.
#3.Easy to Write.
#4.Errors are caught only at runtime,not before.

#Static Typing in Python:
#Types of decided at compile time.
#Once type of variable is declared the type of vaiable cannot be changed.
#Programs usually run faster because types are fixed.
#Less flexible, requires more code.