#Write a program showing bitwise AND, OR, XOR, NOT, shift.

a = 5 
b = 3  

print("a =", a, "binary:", bin(a))
print("b =", b, "binary:", bin(b))

#Bitwise AND
print("a & b =", a & b, "binary:", bin(a & b))

# Bitwise OR
print("a | b =", a | b, "binary:", bin(a | b))

# Bitwise XOR
print("a ^ b =", a ^ b, "binary:", bin(a ^ b))

# Bitwise NOT
print("~a =", ~a, "binary:", bin(~a))

# Left Shift
print("a << 1 =", a << 1, "binary:", bin(a << 1))

# Right Shift
print("a >> 1 =", a >> 1, "binary:", bin(a >> 1))
